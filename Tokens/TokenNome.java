package Tokens;

public enum TokenNome {
    ID,          // Identificadores
    NREAL,       // Números reais
    NINT,        // Números inteiros
    NSTRING,     // Strings delimitadas por aspas
    OP,          // Operadores
    COMENTARIO,  // Comentários
    ESCRITA,     // Função prompt()
    LEITURA,     // Função console.log()
    DELIMITADOR, // Delimitadores
    CONDICIONAL, // Palavras reservadas relacionadas a estruturas condicionais
    VARIAVEIS,   // Palavras reservadas relacionadas à declaração de variáveis
    REPETICAO,   // Palavras reservadas relacionadas a estruturas de repetição
    BOOLEAN,     // Valores booleanos
    PALAVRA_RESERVADA, // Outras palavras reservadas
}

